// Хелпер: вернуть перевод фразы на нужный язык
export function getPhraseTranslation(
  phrase: UmrahPhrase,
  lang: string,
): string {
  if (lang === 'ru') return phrase.ru;
  if (lang === 'en') return phrase.en;
  if (lang === 'uz') return phrase.uz;
  if (lang === 'tj') return phrase.tj;
  return phrase.ru; // fallback (ar → ru)
}

// Хелпер: заголовок секции на нужном языке
export function getSectionTitle(sec: UmrahSection, lang: string): string {
  if (lang === 'ru') return sec.title_ru;
  if (lang === 'en') return sec.title_en;
  if (lang === 'uz') return sec.title_uz;
  if (lang === 'tj') return sec.title_tj;
  return sec.title_ru;
}
